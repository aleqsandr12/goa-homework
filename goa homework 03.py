#კომენტარების სახით ახსენი რომელ მონაცემთა ტიპებთან შეგვიძლია გამოვიყენოთ გარკვეული შედარების ოპერატორები



# == (ტოლობა) და != (არატოლობა)
# შეგვიძლია გამოვიყენოთ თითქმის ყველა მონაცემთა ტიპთან:
a = 5 == 5          # True (int)
b = "abc" != "def"  # True (str)
c = [1, 2] == [1, 2] # True (list – ელემენტების შედარება)
d = True == False   # False (bool)
e = None == None    # True (None ტიპი)

# <, >, <=, >= (მეტობა, ნაკლებობა და მათი კომბინაციები)
# გამოიყენება იმ ტიპებზე, რომლებსაც აქვთ "შედარების ლოგიკა":
x = 5 < 10          # True (int)
y = 3.5 >= 2.1      # True (float)
z = "apple" < "banana"  # True (სტრინგები ანბანური წესით)
# შემდეგი არ იმუშავებს:
# [1, 2] < [1, 3]    # მუშაობს, მაგრამ შესაძლოა არ იყოს ინტუიციური ყველა ტიპზე
# {'a': 1} < {'b': 2}  # TypeError – ლექსიკონებს არ ადარებს პირდაპირ

# შედარება განსხვავებულ ტიპებს შორის:
# Python-ში int და float ერთმანეთს ადარებს:
result = 5 == 5.0     # True
# მაგრამ ზოგჯერ სხვადასხვა ტიპის შედარება გამოიწვევს შეცდომას:
# "5" < 10           # TypeError – სტრინგი და რიცხვი არ შეიძლება შეადარო

# ოპერატორების შეჯამება:
# ==, != – თითქმის ყველა ტიპზე მუშაობს
# <, >, <=, >= – მუშაობს რიცხვებზე (int, float), სტრინგებზე და ზოგჯერ სიის ტიპებზე,
# მაგრამ არა ყველა ტიპზე (მაგ: dictionaries ვერ შეადარებ ერთმანეთს ამ ოპერატორებით)
